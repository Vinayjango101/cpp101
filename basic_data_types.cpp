#include <iostream>
#include <string>

using namespace std;

int main()
{
    int Mynum = 5; // Integer type named Mynum with value 5.
    float Myfloatnum = 5.00; // float type named Myfloatnum with value 5.00.
    double Mydoublenum = 5.01; // double type named Mydoublenum with value of 5.01.
    char Myletter = 'V'; // char type named Myletter with value of letter V.
    bool Mybool = true; // Boolean type named Mybool with value true i.e 1.
    string Mystring = "hello"; // String type named Mystring with value of hello.

    cout << Mynum << endl;
    cout << Myfloatnum << endl;
    cout << Mydoublenum << endl;
    cout << Myletter << endl;
    cout << Mybool << endl;
    cout << Mystring << endl;

}