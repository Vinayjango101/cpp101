#include <iostream>

using namespace std;

int main()
{
    // A Boolean is a Datatype declared with the "bool" keyword and can only take the values true or false.
    // When the value is returned, true = 1 and false = 0.

    bool iscodingfun = true;
    bool iscodingboring = false;
    cout << iscodingfun << endl; // outputs 1 (true)
    cout << iscodingboring << endl; // outputs 2 (false)

}